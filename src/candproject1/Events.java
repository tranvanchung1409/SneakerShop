    private void XoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_XoaMouseClicked
        // TODO add your handling code here:

        if (tenSK.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chọn sự kiện cần xóa!");
        } else {
            int ketqua = JOptionPane.showConfirmDialog(this, "Xóa sự kiện?", "Chú ý", JOptionPane.YES_NO_OPTION);
            if (ketqua == JOptionPane.YES_OPTION) {
                Con = ClassData.ConnectDb();
                try {
                    String tensk = tenSK.getText();
                    String Query = "Delete from banhang.Sukien where tenSK ='" + tensk + "'";
                    Statement Add = Con.createStatement();
                    Add.executeUpdate(Query);
                    ChonSK();
                    JOptionPane.showMessageDialog(this, "Xóa sự kiện thành công!");
                    setSK();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_XoaMouseClicked

