    private void ThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ThemMouseClicked
        // TODO add your handling code here:
        if (tenSK.getText().isEmpty() || thoigianSK.getText().isEmpty() || noidungSK.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nhập Thiếu Thông Tin!");
        } else {
            int ketqua = JOptionPane.showConfirmDialog(this, "Thêm Sản Phẩm?", "Chú ý", JOptionPane.YES_NO_OPTION);
            if (ketqua == JOptionPane.YES_OPTION) {
                Con = ClassData.ConnectDb();
                try {
                    PreparedStatement add = Con.prepareStatement("insert into Sukien values(?,?,?,?)");
                    add.setString(1, null);
                    add.setString(2, tenSK.getText());
                    add.setString(3, thoigianSK.getText());
                    add.setString(4, noidungSK.getText());
                    int row = add.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Thêm Sự Kiện Thành Công!");
                    Con.close();
                    ChonSK();
                    setSK();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_ThemMouseClicked

