    public void bangDoanhThuSP() {

        DefaultTableModel model = new DefaultTableModel(new String[]{"Mã Sản Phẩm", "Tên Sản Phẩm", "Đã Bán", "Còn", "Doanh Thu"}, 0);
        Con = ClassData.ConnectDb();
        try {
            St = Con.createStatement();
            Rs = St.executeQuery("Select sanpham.maSP, sanpham.tenSP, hoadon.soluongHD, sanpham.soSP , sum(hoadon.thanhtienHD) from banhang.danhsachhoadon, banhang.hoadon, banhang.sanpham where sanpham.maSP = hoadon.maSP  and hoadon.maHD=danhsachhoadon.maHD and danhsachhoadon.ngaylapHD like CONCAT('%2021-%') group by maSP order by sum(hoadon.thanhtienHD) desc");
            while (Rs.next()) {
                String d = Rs.getString("sanpham.maSP");
                String e = Rs.getString("sanpham.tenSP");
                String f = Rs.getString("hoadon.soluongHD");
                String g = Rs.getString("sanpham.soSP");
                String h = Rs.getString("sum(hoadon.thanhtienHD)");
                model.addRow(new Object[]{d, e, f, g, h});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        DoanhThuSP.setModel(model);
    }

    public void bangDoanhThuNV() {

        DefaultTableModel model = new DefaultTableModel(new String[]{"Mã Nhân Viên", "Tên Nhân Viên", "Doanh Thu"}, 0);
        Con = ClassData.ConnectDb();
        try {
            St = Con.createStatement();
            Rs = St.executeQuery("Select nhanvien.maNV, nhanvien.tenNV, sum(danhsachhoadon.giaHD) from banhang.danhsachhoadon, banhang.nhanvien where nhanvien.maNV = danhsachhoadon.maNV and danhsachhoadon.ngaylapHD like CONCAT('%2021-%') group by maNV order by sum(danhsachhoadon.giaHD) desc");
            while (Rs.next()) {
                String d = Rs.getString("nhanvien.maNV");
                String e = Rs.getString("nhanvien.tenNV");
                String f = Rs.getString("sum(danhsachhoadon.giaHD)");
                model.addRow(new Object[]{d, e, f});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        DoanhThuNV.setModel(model);
    }

