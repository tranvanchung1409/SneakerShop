    private void jComboBox5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox5ActionPerformed
        int index = jComboBox5.getSelectedIndex();

        // Clear table
        DoanhThuSP.setModel(new DefaultTableModel());
        // Model for Table
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public Class<?> getColumnClass(int column) {
                switch (column) {
                    case 0:
                        return String.class;
                    case 1:
                        return String.class;
                    case 2:
                        return String.class;
                    case 3:
                        return String.class;
                    default:
                        return String.class;
                }
            }
        };
        DoanhThuSP.setModel(model);

        // Add Column
        model.addColumn("Mã Sản Phẩm");
        model.addColumn("Tên Sản Phẩm");
        model.addColumn("Đã bán");
        model.addColumn("Còn");
        model.addColumn("Doanh Thu");

        if (index > 0) {
            String a = String.format("%02d", index);
            Con = ClassData.ConnectDb();
            try {
                St = Con.createStatement();
                Rs = St.executeQuery("Select sanpham.maSP, sanpham.tenSP, hoadon.soluongHD, sanpham.soSP , sum(hoadon.thanhtienHD) from banhang.danhsachhoadon, banhang.hoadon, banhang.sanpham where sanpham.maSP = hoadon.maSP  and hoadon.maHD=danhsachhoadon.maHD  and danhsachhoadon.ngaylapHD like CONCAT('%2021-','" + a + "%') group by maSP order by sum(hoadon.thanhtienHD) desc");
                int row = 0;

                while ((Rs != null) && (Rs.next())) {
                    model.addRow(new Object[0]);
                    model.setValueAt(Rs.getInt("sanpham.maSP"), row, 0);
                    model.setValueAt(Rs.getString("sanpham.tenSP"), row, 1);
                    model.setValueAt(Rs.getString("hoadon.soluongHD"), row, 2);
                    model.setValueAt(Rs.getString("sanpham.soSP"), row, 3);
                    model.setValueAt(Rs.getString("sum(hoadon.thanhtienHD)"), row, 4);
                    row++;
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            bangDoanhThuSP();
        }
    }//GEN-LAST:event_jComboBox5ActionPerformed

