    public String getData() {
        ArrayList<String> list = new ArrayList<String>();
        String r = "";
        Con = ClassData.ConnectDb();
        try {
            St = Con.createStatement();
            Rs = St.executeQuery("Select * from banhang.Khachhang");
            while (Rs.next()) {

                list.add(Rs.getString("emailKH"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        for (String s : list) {
            r += s + "," + " ";
        }
        return r;
    }

