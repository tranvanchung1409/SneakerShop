    public void ChonSK() {
        DefaultTableModel model = new DefaultTableModel(new String[]{"Tên Sự Kiện", "Thời Gian", "Nội Dung"}, 0);
        Con = ClassData.ConnectDb();
        try {
            St = Con.createStatement();
            Rs = St.executeQuery("Select * from banhang.Sukien");
            while (Rs.next()) {
                String d = Rs.getString("tenSK");
                String e = Rs.getString("thoigianSK");
                String f = Rs.getString("noidungSK");

                model.addRow(new Object[]{d, e, f});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        BangSK.setModel(model);
    }

    public void ChonKH() {
        DefaultTableModel model = new DefaultTableModel(new String[]{"Mã Khách Hàng", "Tên Khách Hàng", "Email"}, 0);
        Con = ClassData.ConnectDb();
        try {
            St = Con.createStatement();
            Rs = St.executeQuery("Select * from banhang.Khachhang where `emailKH` IS NOT NULL ");
            while (Rs.next()) {
                String c = Rs.getString("maKH");
                String d = Rs.getString("tenKH");
                String e = Rs.getString("emailKH");

                model.addRow(new Object[]{c, d, e});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        BangEmail.setModel(model);
    }

